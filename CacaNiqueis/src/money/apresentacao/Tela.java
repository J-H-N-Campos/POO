    private void btnApostarActionPerformed(java.awt.event.ActionEvent evt)//GEN-FIRST:event_btnApostarActionPerformed
    {//GEN-HEADEREND:event_btnApostarActionPerformed
        double premio = this.maquina.apostar();
        if (premio == 0)
        {
            this.campoResultado.setText("Continue tentando");
        } else
        {
            DecimalFormat df = new DecimalFormat("###,##0.00");
            this.campoResultado.setText("Ganhou R$ " + df.format(premio));
        }
        this.atualizarTela();
    }//GEN-LAST:event_btnApostarActionPerformed

