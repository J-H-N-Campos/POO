    public void atualizarTela()
    {
        DecimalFormat df = new DecimalFormat("###,##0.00");
        this.campoContador.setText(maquina.getContadorApostas() + "");
        this.campoLucro.setText("R$ " + df.format(maquina.getLucro()));
        this.campoPremios.setText("R$ " + df.format(maquina.getPremiosDistribuidos()));
        this.campoSaldo.setText("R$ " + df.format(maquina.getSaldoCaixa()));
        if (this.maquina.getSaldoCaixa() >= 1000 && this.maquina.getSaldoCaixa() < 10000)
        {
            this.btnApostar.setEnabled(true);
        } else
        {
            this.btnApostar.setEnabled(false);
        }
    }

