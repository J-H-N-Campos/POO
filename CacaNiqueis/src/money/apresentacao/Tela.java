    private void btnSangriaActionPerformed(java.awt.event.ActionEvent evt)//GEN-FIRST:event_btnSangriaActionPerformed
    {//GEN-HEADEREND:event_btnSangriaActionPerformed
        Autenticavel aut = new Autenticavel()
        {
            @Override
            public void inicializar()
            {
                if (rb1000.isSelected())
                {
                    maquina.sangria();
                } else
                {
                    maquina.sangriaTotal();
                }
                atualizarTela();
            }
        };

        Lancador l = new Lancador(aut);
        l.start();
    }//GEN-LAST:event_btnSangriaActionPerformed

